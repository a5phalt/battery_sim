import type { BatteryTypeCode } from './BatteryType';

// Пресет — это сохранённая настройка параметров батареи.
// Хранит только "железо" (тип, ёмкость, напряжение, ток).
// SOC и режим — задаются отдельно при запуске симуляции
export interface BatteryPreset {
  id: string;                   // Уникальный ID пресета
  name: string;                 // Название (придумывает пользователь)
  typeId: BatteryTypeCode;      // Ссылка на тип батареи
  capacity: number;             // Ёмкость (А·ч)
  minVoltage: number;           // Минимальное напряжение (В)
  maxVoltage: number;           // Максимальное напряжение (В)
  current: number;              // Ток (А)
}