// Одна точка на графике (один шаг расчёта).
// Массив таких точек = весь график.
export interface SimulationPoint {
  time: number;                 // Время от начала (с)
  soc: number;                  // Уровень заряда (%)
  voltage: number;              // Напряжение (В)
  current: number;              // Ток (А)
  power: number;                // Мощность (Вт) = U × I
  energy: number;               // Энергия за эот шаг (Дж) = P × Δt
}